#!/usr/bin/env node
// Reads bootstrap config from SST outputs or existing bootstrap.json and writes
// a TypeScript constants file for build-time defaults.
"use strict";

const fs = require("node:fs");
const path = require("node:path");

const repoRoot = path.resolve(__dirname, "..", "..", "..");
const sstOutputsPath = path.join(repoRoot, "infra", ".sst", "outputs.json");
const bootstrapJsonPath = path.join(repoRoot, ".ade", "hosted", "bootstrap.json");
const outDir = path.join(__dirname, "..", "src", "generated");
const outPath = path.join(outDir, "bootstrapDefaults.ts");

function fromSstOutputs() {
  if (!fs.existsSync(sstOutputsPath)) return null;

  try {
    const raw = JSON.parse(fs.readFileSync(sstOutputsPath, "utf8"));
    if (!raw || typeof raw !== "object" || !raw.apiUrl) return null;

    const clerk = raw.clerk ?? {};
    return {
      stage: String(raw.stage ?? "unknown"),
      apiBaseUrl: String(raw.apiUrl ?? ""),
      region: String(raw.region ?? ""),
      clerkPublishableKey: String(clerk.publishableKey ?? ""),
      clerkOauthClientId: String(clerk.oauthClientId ?? ""),
      clerkIssuer: String(clerk.issuer ?? ""),
      clerkFrontendApiUrl: String(clerk.frontendApiUrl ?? ""),
      clerkOauthMetadataUrl: String(clerk.oauthMetadataUrl ?? ""),
      clerkOauthAuthorizeUrl: String(clerk.oauthAuthorizeUrl ?? ""),
      clerkOauthTokenUrl: String(clerk.oauthTokenUrl ?? ""),
      clerkOauthRevocationUrl: String(clerk.oauthRevocationUrl ?? ""),
      clerkOauthUserInfoUrl: String(clerk.oauthUserInfoUrl ?? ""),
      clerkOauthScopes: String(clerk.oauthScopes ?? "openid profile email offline_access"),
    };
  } catch {
    return null;
  }
}

function fromBootstrapJson() {
  if (!fs.existsSync(bootstrapJsonPath)) return null;

  try {
    const parsed = JSON.parse(fs.readFileSync(bootstrapJsonPath, "utf8"));
    if (!parsed || typeof parsed !== "object" || !parsed.apiBaseUrl) return null;

    // Return only the fields we need (strip generatedAt)
    const {generatedAt, ...rest} = parsed;
    return rest;
  } catch {
    return null;
  }
}

const config = fromSstOutputs() ?? fromBootstrapJson() ?? null;

fs.mkdirSync(outDir, { recursive: true });

const banner = "// AUTO-GENERATED by scripts/generate-bootstrap-defaults.cjs \u2014 do not edit\n";
const body = config
  ? `${banner}export const BOOTSTRAP_DEFAULTS = ${JSON.stringify(config, null, 2)} as const;\n`
  : `${banner}export const BOOTSTRAP_DEFAULTS = null;\n`;

fs.writeFileSync(outPath, body, "utf8");

if (config) {
  console.log(`[generate-bootstrap-defaults] Wrote defaults (stage: ${config.stage}) to ${outPath}`);
} else {
  console.log(`[generate-bootstrap-defaults] No source found \u2014 wrote null defaults to ${outPath}`);
}
